import json, re
import numpy as np
import torch
from huggingface_hub import hf_hub_download
from safetensors.torch import load_file as load_safetensors

KEY_PATTERN = re.compile(
    r"layers\.(\d+)\..*?\.(q_proj|k_proj|v_proj|o_proj|gate_proj|up_proj|down_proj)"
    r"\.lora_(A|B)(?:\.\w+)?\.weight$"
)

def load_adapter(repo_id):
    try:
        cfg_path = hf_hub_download(repo_id, "adapter_config.json")
    except Exception as e:
        print(f"  [SKIP] {repo_id}: could not fetch adapter_config.json ({e})")
        return None
    cfg = json.load(open(cfg_path))

    weights = None
    for fname, loader in [("adapter_model.safetensors", load_safetensors),
                           ("adapter_model.bin", lambda p: torch.load(p, map_location="cpu"))]:
        try:
            wpath = hf_hub_download(repo_id, fname)
            weights = loader(wpath)
            break
        except Exception:
            continue
    if weights is None:
        print(f"  [SKIP] {repo_id}: no adapter_model.safetensors or .bin found")
        return None

    return {"repo_id": repo_id, "r": cfg.get("r"), "base_model": cfg.get("base_model_name_or_path"),
            "target_modules": set(cfg.get("target_modules", [])), "weights": weights}

def extract_AB(weights):
    out = {}
    for key, tensor in weights.items():
        m = KEY_PATTERN.search(key)
        if not m:
            continue
        layer_idx, module, ab = int(m.group(1)), m.group(2), m.group(3)
        out.setdefault((layer_idx, module), {})[ab] = tensor.float()
    return out

def subspace_overlap(M, N):
    if tuple(M.shape) != tuple(N.shape):
        return None  # different ambient dim -- not comparable (e.g. 7B vs 13B base)
    r = M.shape[1]
    M_np = M.detach().cpu().numpy() if torch.is_tensor(M) else np.asarray(M)
    N_np = N.detach().cpu().numpy() if torch.is_tensor(N) else np.asarray(N)
    Q1, _ = np.linalg.qr(M_np)
    Q2, _ = np.linalg.qr(N_np)
    return float(np.linalg.norm(Q1.T @ Q2, ord="fro") ** 2 / r)

def random_baseline(ambient_dim, r, n_trials=20):
    vals = []
    for _ in range(n_trials):
        Q1, _ = np.linalg.qr(np.random.randn(ambient_dim, r))
        Q2, _ = np.linalg.qr(np.random.randn(ambient_dim, r))
        vals.append(np.linalg.norm(Q1.T @ Q2, "fro") ** 2 / r)
    return float(np.mean(vals))

def analyze_pair(repo_a, repo_b, rank_label):
    print(f"\n=== rank={rank_label}: {repo_a}  vs  {repo_b} ===")
    a1, a2 = load_adapter(repo_a), load_adapter(repo_b)
    if a1 is None or a2 is None:
        return []

    print(f"  {repo_a}: r={a1['r']}, base={a1['base_model']}, modules={sorted(a1['target_modules'])}")
    print(f"  {repo_b}: r={a2['r']}, base={a2['base_model']}, modules={sorted(a2['target_modules'])}")
    if a1["r"] != a2["r"]:
        print(f"  [WARN] declared rank mismatch ({a1['r']} vs {a2['r']}) -- treat this pair as exploratory only")

    shared_modules = a1["target_modules"] & a2["target_modules"]
    if not shared_modules:
        print("  [SKIP] no overlapping target_modules"); return []

    AB1, AB2 = extract_AB(a1["weights"]), extract_AB(a2["weights"])
    shared_layers = sorted({k[0] for k in AB1} & {k[0] for k in AB2})
    if not shared_layers:
        print("  [SKIP] no matching layer indices in the two checkpoints"); return []

    rows = []
    for module in sorted(shared_modules):
        OAs, OBs = [], []
        for layer in shared_layers:
            d1, d2 = AB1.get((layer, module)), AB2.get((layer, module))
            if not d1 or not d2 or "A" not in d1 or "B" not in d1 or "A" not in d2 or "B" not in d2:
                continue
            oA = subspace_overlap(d1["A"].T, d2["A"].T)
            oB = subspace_overlap(d1["B"], d2["B"])
            if oA is None or oB is None or oA == 1 or oB == 1:
                continue  # shape mismatch -- different base model hidden_size
            OAs.append(oA); OBs.append(oB)
        if OAs:
            mA, mB = float(np.mean(OAs)), float(np.mean(OBs))
            print(f"  {module:10s} n_layers={len(OAs):3d}  mean O_A={mA:.4f}  mean O_B={mB:.4f}  gap={mB-mA:+.4f}")
            rows.append((module, len(OAs), mA, mB))
        else:
            print(f"  {module:10s} [SKIP] shape mismatch on every layer (likely different hidden_size)")
    return rows

import json
import logging
import itertools
from collections import defaultdict
from concurrent.futures import ThreadPoolExecutor, as_completed
import urllib.request
from huggingface_hub import HfApi

logging.getLogger("huggingface_hub").setLevel(logging.ERROR)

VALID_RANKS = {8, 16, 32, 64}
MAX_ADAPTERS_PER_BUCKET = 6
MAX_PAIRS_PER_RANK = 8
OUTPUT_JSON_PATH = "adapter_groups.json"


def process_single_model(model_id):
    config_url = f"https://huggingface.co/{model_id}/raw/main/adapter_config.json"
    try:
        req = urllib.request.Request(config_url, headers={"User-Agent": "Mozilla/5.0"})
        with urllib.request.urlopen(req, timeout=3) as response:
            config = json.loads(response.read().decode("utf-8"))

        rank = config.get("r")
        alpha = config.get("lora_alpha")
        base_model = config.get("base_model_name_or_path")
        target_modules = config.get("target_modules")

        if not base_model or rank not in VALID_RANKS or not target_modules:
            return None

        base_model_cleaned = base_model.split("/")[-1].replace("-hf", "").lower()

        if isinstance(target_modules, list):
            target_tuple = tuple(sorted(str(m).lower() for m in target_modules))
        elif isinstance(target_modules, str):
            target_tuple = (target_modules.lower(),)
        else:
            return None

        return {
            "key": (base_model_cleaned, target_tuple, rank),
            "model_id": model_id,
            "alpha": alpha,
        }
    except Exception:
        return None


def scan_peft_models(limit=8000, max_workers=32):
    api = HfApi()
    print("Fetching a broad sample of PEFT models from Hugging Face...")
    models = api.list_models(filter="peft", limit=limit)
    model_ids = [m.id for m in models]

    buckets = defaultdict(list)
    print(f"Scanning configs for {len(model_ids)} models using parallel threads...")
    with ThreadPoolExecutor(max_workers=max_workers) as executor:
        futures = {executor.submit(process_single_model, m_id): m_id for m_id in model_ids}
        for completed_count, future in enumerate(as_completed(futures), 1):
            if completed_count % 1000 == 0:
                print(f"  Processed {completed_count}/{len(model_ids)} models...")
            res = future.result()
            if res:
                buckets[res["key"]].append({"model_id": res["model_id"], "alpha": res["alpha"]})
    return buckets


def get_username(model_id):
    """Extract the Hugging Face namespace/username from a model_id."""
    return model_id.split("/")[0] if "/" in model_id else model_id


def build_adapter_groups(buckets):
    """
    Turn config-matched buckets into ADAPTER_GROUPS = {rank: [(id1, id2), ...]},
    pairing every adapter against every other adapter sharing the same cleaned
    base model, target_modules, and rank. Pairs where both adapters share the
    same Hugging Face username are excluded -- those aren't independently
    trained in the sense the experiment needs.
    """
    groups = {r: [] for r in sorted(VALID_RANKS)}
    pair_context = []
    pairs_per_rank = defaultdict(int)
    skipped_same_user = 0

    for (base_model, target_tuple, rank), items in sorted(buckets.items(), key=lambda kv: -len(kv[1])):
        if len(items) < 2 or pairs_per_rank[rank] >= MAX_PAIRS_PER_RANK:
            continue
        for item_a, item_b in itertools.combinations(items[:MAX_ADAPTERS_PER_BUCKET], 2):
            if pairs_per_rank[rank] >= MAX_PAIRS_PER_RANK:
                break
            if get_username(item_a["model_id"]) == get_username(item_b["model_id"]):
                skipped_same_user += 1
                continue
            groups[rank].append((item_a["model_id"], item_b["model_id"]))
            pair_context.append((rank, base_model, target_tuple, item_a, item_b))
            pairs_per_rank[rank] += 1

    if skipped_same_user:
        print(f"Skipped {skipped_same_user} same-username pair(s) within matching buckets.")

    return groups, pair_context


def render_python_literal(groups):
    lines = ["ADAPTER_GROUPS = {"]
    for rank in sorted(groups):
        lines.append(f"    {rank}: [")
        for a, b in groups[rank]:
            lines.append(f"        ({a!r}, {b!r}),")
        lines.append("    ],")
    lines.append("}")
    return "\n".join(lines)


def main():
    buckets = scan_peft_models()

    total_adapters = sum(len(v) for v in buckets.values())
    matched_buckets = sum(1 for v in buckets.values() if len(v) >= 2)
    print(f"\nFound {total_adapters} adapters with usable configs, across "
          f"{matched_buckets} buckets that have 2+ adapters sharing "
          f"(base model, target modules, rank).")

    groups, pair_context = build_adapter_groups(buckets)

    print("\n" + "=" * 40)
    print("=== AUTO-SELECTED ADAPTER PAIRS ===")
    print("=" * 40)
    for rank, base_model, target_tuple, item_a, item_b in pair_context:
        print(f"\nRANK {rank} | base_model={base_model} | target_modules={list(target_tuple)}")
        print(f"  [A] https://huggingface.co/{item_a['model_id']}  (alpha={item_a['alpha']})")
        print(f"  [B] https://huggingface.co/{item_b['model_id']}  (alpha={item_b['alpha']})")

    total_pairs = sum(len(v) for v in groups.values())
    print(f"\nTotal pairs selected: {total_pairs}  "
          f"({', '.join(f'rank {r}: {len(groups[r])}' for r in sorted(groups))})")

    with open(OUTPUT_JSON_PATH, "w") as f:
        json.dump({str(r): pairs for r, pairs in groups.items()}, f, indent=2)
    print(f"\nSaved machine-readable version to {OUTPUT_JSON_PATH}")

    print("\n--- Paste this directly into Cell 2 of the Colab notebook ---\n")
    print(render_python_literal(groups))

    return groups


if __name__ == "__main__":
    ADAPTER_GROUPS = main()

import math
from collections import defaultdict
import matplotlib.pyplot as plt

results = {}
for rank_label, pairs in ADAPTER_GROUPS.items():
    results[rank_label] = []
    for repo_a, repo_b in pairs:
        rows = analyze_pair(repo_a, repo_b, rank_label)
        if rows:
            results[rank_label].append(rows)

# Aggregate per (module, rank) -- keep q_proj, v_proj, etc. as separate series
# instead of pooling every module into one mean per rank.
per_module = defaultdict(lambda: defaultdict(lambda: {"OA": [], "OB": []}))
for rank_label, pair_results in results.items():
    for pair_rows in pair_results:
        for module, n_layers, oA, oB in pair_rows:
            per_module[module][rank_label]["OA"].append(oA)
            per_module[module][rank_label]["OB"].append(oB)

print("\n\n=== Summary across ranks, per target module ===")
modules_present = sorted(per_module.keys())
summary = {m: [] for m in modules_present}
for module in modules_present:
    print(f"\n--- {module} ---")
    for rank_label in sorted(per_module[module]):
        OAs = per_module[module][rank_label]["OA"]
        OBs = per_module[module][rank_label]["OB"]
        if not OAs:
            continue
        mA, mB = float(np.mean(OAs)), float(np.mean(OBs))
        print(f"  rank={rank_label:3d}  n_pairs={len(OAs)}  mean O_A={mA:.4f}  mean O_B={mB:.4f}  gap={mB-mA:+.4f}")
        summary[module].append((rank_label, mA, mB))

modules_with_data = [m for m in modules_present if summary[m]]
if not modules_with_data:
    print("No usable per-module data to plot.")
else:
    ncols = min(len(modules_with_data), 4)
    nrows = math.ceil(len(modules_with_data) / ncols)
    fig, axes = plt.subplots(nrows, ncols, figsize=(5 * ncols, 4 * nrows), squeeze=False)
    flat_axes = axes.flatten()
    for ax, module in zip(flat_axes, modules_with_data):
        ranks, mA_list, mB_list = zip(*sorted(summary[module]))
        ax.plot(ranks, mA_list, "o-", label="O_A (input side)")
        ax.plot(ranks, mB_list, "s-", label="O_B (output side)")
        ax.set_xlabel("LoRA rank")
        ax.set_ylabel("mean subspace overlap")
        ax.set_title(module)
        ax.legend()
    for ax in flat_axes[len(modules_with_data):]:
        ax.axis("off")
    plt.tight_layout()
    plt.show()